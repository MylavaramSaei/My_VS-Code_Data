#include <iostream>
using namespace std;

int main() {
    // Declare variables
    int num1, num2, sum;

    // Get input from the user
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    // Calculate sum
    sum = num1 + num2;

    // Display result
    cout << "Sum = " << sum << endl;

    return 0;
}
